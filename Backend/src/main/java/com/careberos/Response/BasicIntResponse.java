package com.careberos.Response;

public class BasicIntResponse {
	private int code;
	private int count;
	
	public BasicIntResponse() {}

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}
	
	
}
